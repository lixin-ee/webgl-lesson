var direction=1;
var count=0;
function turnleg(dog,index,name,isL){
	var component=dog.compoments.get(index);
	var originPosition=name[0];
	component.objectMatrix=glMatrix.multiply(glMatrix._inverseTranslate(originPosition.x,originPosition.y,originPosition.z),component.objectMatrix)
	component.objectMatrix=glMatrix.multiply(glMatrix._xRotate(0.1*direction*isL),component.objectMatrix)
	component.objectMatrix=glMatrix.multiply(glMatrix._translate(originPosition.x,originPosition.y,originPosition.z),component.objectMatrix)
}

//规定狗的朝向始终为物体坐标系的z轴正方向,狗的背面朝向y轴负方向(使得物体坐标系在初始状态和世界坐标系平行)
dog.moveforward=function(dz){
	count++;
	if(count%5==0){
		direction*=-1;
	}

	turnleg(dog,DOG_BL,dogBL,1);
	turnleg(dog,DOG_BR,dogFR,-1);
	turnleg(dog,DOG_FL,dogFL,1);
	turnleg(dog,DOG_FR,dogFR,-1);
	
	dog.objectMatrix=glMatrix.multiply(glMatrix._translate(0,0,dz),dog.objectMatrix)//矩阵逆的性质
}
dog.turnDirection=function(dtheta){
	dog.objectMatrix=glMatrix.multiply(glMatrix._yRotate(dtheta),dog.objectMatrix)//矩阵逆的性质
}
dog.movebackward=function(dx){
	dog.moveforward(-dx);
}
dog.turnside=function(dtheta){
	dog.objectMatrix=glMatrix.multiply(glMatrix._inverseTranslate(325,150,2000),dog.objectMatrix)
	dog.objectMatrix=glMatrix.multiply(glMatrix._xRotate(dtheta),dog.objectMatrix)//矩阵逆的性质
	dog.objectMatrix=glMatrix.multiply(glMatrix._translate(325,150,2000),dog.objectMatrix);		
}
